function addition(num1:number,num2:number)
{
    return(num1 + num2)
}
console.log("add(5,5) = " + addition(5,5))
function substraction(a:number,b:number){
    return(a - b)
}
console.log("sub(10,5) = " + substraction(10,5))
function multiply(a:number,b:number){
    return(a * b)
}
console.log("mul(5,5) = " + multiply(5,5))